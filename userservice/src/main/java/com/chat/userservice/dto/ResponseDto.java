package com.chat.userservice.dto;


public class ResponseDto {
    private String name;
    private String age;

    public ResponseDto(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
